    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String selectedCityName = (String) cityJComboBox.getSelectedItem();
        String selectedHospitalName = (String) HospitalJComboBox.getSelectedItem();
        String selectedDoctorName = (String) doctorJComboBox.getSelectedItem();
         HospitalCity selectedCity = null;
        Hospital selectedHospital = null;
        Doctor selectedDoctor=null;
        
        for (HospitalCity city : system.getCities()){
            if (city.getCityName().equalsIgnoreCase(selectedCityName)){
                 selectedCity = city;
            }
        }      
        for (Hospital hospital : selectedCity.getHospitals()){
            if (hospital.getHospitalName().equalsIgnoreCase(selectedHospitalName)){
                selectedHospital = hospital;
            }
        }
        for (Doctor doctor : selectedHospital.getDoctors()){
            if (doctor.getName().equalsIgnoreCase(selectedDoctorName)){
                selectedDoctor = doctor;
            }
        }
       
        
        OPPanel2 panel = new OPPanel2(displayJpanel,system);
        displayJpanel.add("OUTPATIENT", panel);
        CardLayout layout = (CardLayout) displayJpanel.getLayout();
        layout.next(displayJpanel);
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

