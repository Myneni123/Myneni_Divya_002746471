    private void initializeCities() {
        
        List<HospitalCity> cities = system.getCities();
        for(HospitalCity city:cities){
            cityJComboBox.addItem(city.getCityName());
        }
    }
    private void initializehospitals() {
        HospitalJComboBox.removeAllItems();
        String selectedCityName = (String) cityJComboBox.getSelectedItem();
        for(HospitalCity city : system.getCities()) {
            if(city.getCityName().equalsIgnoreCase(selectedCityName)){
                this.city = city;                
                for(Hospital hospital: city.getHospitals()){
                    HospitalJComboBox.addItem(hospital.getHospitalName());
                    
                }
            }
        }
        initializedoctors();
     }
    private void initializedoctors() {
        doctorJComboBox.removeAllItems();
        String selectedHospitalName = (String) HospitalJComboBox.getSelectedItem();
        for(Hospital hospital: this.city.getHospitals()) {
            if(hospital.getHospitalName().equalsIgnoreCase(selectedHospitalName)){
                this.hospital = hospital;
                
                for(Doctor doctor : hospital.getDoctors()){
                    doctorJComboBox.addItem(doctor.getSpecialization());
                }
            }
        }
    }


