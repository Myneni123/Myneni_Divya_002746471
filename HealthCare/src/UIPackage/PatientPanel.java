    private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) patientsJTable.getModel();
        model.setRowCount(0);
        
        for(Person person:House.getPersons()){
            Object[] row = new Object[4];
            row[0]=person;
            row[1]=person.getAge();
            row[2]=person.getGender();
            row[3]=person.isMarried();
            
            model.addRow(row);
        }
    }
    
