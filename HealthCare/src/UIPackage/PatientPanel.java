    private void showPatientInfo(int selectedRow) {
        
        DefaultTableModel model = (DefaultTableModel) patientsJTable.getModel();
        Person person = (Person) model.getValueAt(selectedRow, 0);
        navigateToPatientHistory(person);
    }
    
